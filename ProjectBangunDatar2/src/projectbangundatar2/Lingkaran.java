public class Lingkaran {
    double hitungLuas(int r) {
        return Math.PI * r * r;
    }
    
    double hitungKeliling(int r) {
        return 2 * Math.PI * r;
    }
    
    double hitungLuas(double r) {
        return Math.PI * r * r;
    }
    
    double hitungKeliling(double r) {
        return 2 * Math.PI * r;
    }
}
